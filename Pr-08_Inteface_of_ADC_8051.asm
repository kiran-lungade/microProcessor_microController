; TITLE: INTERFACING OF ADC WITH 8051

	ALE EQU P3.4
	OE EQU P3.7
	
	START EQU P3.5
	EOC EQU P3.6
	SEL_A EQU P3.1
	SEL_B EQU P3.2
	SEL_C EQU P3.3
	ADC_DATA EQU P1
	
	ORG 0H
	MOV ADC_DATA, #0FFH
	SETB EOC
	CLR ALE
	CLR 0E
	CLR START

	MAIN LOOP:
		SETB SEL_A
		CLR SEL_B
		CLR SEL_C
		SET ALE
		SETB START
		CLR ALE
		CLR START
		JB EOC, $
		JNB EOC, $
		SETB 0E
		MOV A, ADC_DATA
		CLR 0E
		SJMP MAIN LOOP
		END